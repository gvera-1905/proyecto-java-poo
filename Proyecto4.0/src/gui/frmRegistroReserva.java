    private void btnBuscarReservaPasajerosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarReservaPasajerosActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigoReserva.getText().trim();
    if (codigo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el código de la reserva.");
        return;
    }

    Reserva res = gestionReserva.buscarPorCodigo(codigo);
    if (res != null) {
        listarPasajerosDeReserva(res);
        JOptionPane.showMessageDialog(this, "Reserva cargada. Pasajeros actuales en el grupo: " + res.getTotalClientes());
    } else {
        JOptionPane.showMessageDialog(this, "Reserva no encontrada.");
    }
    }//GEN-LAST:event_btnBuscarReservaPasajerosActionPerformed

    private void btnAsociarPasajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsociarPasajeroActionPerformed
        // TODO add your handling code here:
        String codigoReserva = txtCodigoReserva.getText().trim();
        String dniPasajero = txtDniPasajero.getText().trim();

        if (codigoReserva.isEmpty() || dniPasajero.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Especifique el Código de Reserva y el DNI del Pasajero.");
            return;
        }

        Reserva res = gestionReserva.buscarPorCodigo(codigoReserva);
        if (res == null) {
            JOptionPane.showMessageDialog(this, "La reserva especificada no existe.");
            return;
        }

        Cliente clienteEncontrado = gestionCliente.buscarPorDni(dniPasajero);
        if (clienteEncontrado == null) {
            JOptionPane.showMessageDialog(this, "El cliente con DNI " + dniPasajero + " no está registrado en el sistema.\nPor favor, regístrelo primero en el Mantenimiento de Clientes.");
            return;
        }

        if (res.agregarClientes(clienteEncontrado)) {
            JOptionPane.showMessageDialog(this, "¡Pasajero " + clienteEncontrado.getNombres() + " asociado con éxito al grupo!");
            listarPasajerosDeReserva(res);
            txtDniPasajero.setText("");
            txtDniPasajero.requestFocus();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo agregar: Se ha alcanzado el límite máximo de pasajeros calculados para esta reserva.");
        }
    }//GEN-LAST:event_btnAsociarPasajeroActionPerformed

