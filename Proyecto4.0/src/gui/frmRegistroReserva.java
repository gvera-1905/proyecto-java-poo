    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String codigoReserva = txtCodigoReserva.getText().trim();
    String codigoPaquete = txtCodigoPaquete.getText().trim();
    
    if (codigoReserva.isEmpty() || codigoPaquete.isEmpty() || txtAdultos.getText().isEmpty() || txtMenores.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.");
        return;
    }

    PaqueteTuristico paqueteElegido = gestionPaquete.buscarPorCodigo(codigoPaquete);
    if (paqueteElegido == null) {
        JOptionPane.showMessageDialog(this, "Error: El código de paquete ingresado no existe.");
        return;
    }

    int pasajerosAdultos = Integer.parseInt(txtAdultos.getText().trim());
    int pasajerosMenores = Integer.parseInt(txtMenores.getText().trim());
    double descMenores = Double.parseDouble(txtDescuentoMenores.getText().trim());

    Reserva nuevaReserva = new Reserva(codigoReserva, paqueteElegido, pasajerosAdultos, pasajerosMenores, descMenores);

    if (!nuevaReserva.registro()) {
        JOptionPane.showMessageDialog(this, "No se puede registrar: El paquete no cuenta con cupos suficientes.", "Sin Cupo", JOptionPane.ERROR_MESSAGE);
        return;
    }

    if (gestionReserva.registrar(nuevaReserva)) {
        JOptionPane.showMessageDialog(this, "Reserva registrada con éxito. Estado: " + nuevaReserva.getEstadoReserva());
        listarReservas();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Error: El código de reserva ya se encuentra registrado.");
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

