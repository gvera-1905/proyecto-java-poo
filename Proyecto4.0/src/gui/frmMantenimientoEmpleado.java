    public void listarEmpleados() {
        modelo.setRowCount(0);
        for (int i = 0; i < gestionEmpleado.obtenerTotalEmpleados(); i++) {
            Empleado emp = gestionEmpleado.obtenerEmpleado(i);
            
            Object[] fila = new Object[5];
            fila[0] = emp.getDNI();
            fila[1] = emp.getNombres();
            fila[2] = emp.getApellidos();
            fila[3] = emp.getUsuario();
            fila[4] = emp.getRol();
            
            modelo.addRow(fila);
        }
    }
        
