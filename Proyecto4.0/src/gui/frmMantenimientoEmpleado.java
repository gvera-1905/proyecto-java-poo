    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:

        if (txtDni.getText().trim().isEmpty() || 
            txtNombres.getText().trim().isEmpty() || 
            txtApellidos.getText().trim().isEmpty() || 
            txtUsuario.getText().trim().isEmpty() || 
            String.valueOf(txtContrasenia.getPassword()).trim().isEmpty()) {
        
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }


        String dni = txtDni.getText().trim();
        String nombres = txtNombres.getText().trim();
        String apellidos = txtApellidos.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String contrasenia = String.valueOf(txtContrasenia.getPassword()).trim();
        String rol = cboRol.getSelectedItem().toString();


        Empleado nuevoEmp = new Empleado(usuario, contrasenia, rol, dni, nombres, apellidos);

        boolean exito = gestionEmpleado.registrar(nuevoEmp);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Empleado registrado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            listarEmpleados();
            btnLimpiarActionPerformed(null); 
        } else {
            JOptionPane.showMessageDialog(this, "Error: El DNI ya se encuentra registrado o el arreglo está lleno.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
                if (txtDni.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un DNI válido para actualizar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String dni = txtDni.getText().trim();
        String nombres = txtNombres.getText().trim();
        String apellidos = txtApellidos.getText().trim();
        String usuario = txtUsuario.getText().trim();
        String contrasenia = String.valueOf(txtContrasenia.getPassword()).trim();
        String rol = cboRol.getSelectedItem().toString();

        Empleado empModificado = new Empleado(usuario, contrasenia, rol, dni, nombres, apellidos);

        boolean exito = gestionEmpleado.actualizar(empModificado);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Datos del empleado actualizados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            listarEmpleados();
            btnLimpiarActionPerformed(null);
        } else {
            JOptionPane.showMessageDialog(this, "No se encontró ningún empleado con el DNI especificado.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (txtDni.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese o seleccione el DNI del empleado que desea eliminar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
        }

        String dni = txtDni.getText().trim();

        int respuesta = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar al empleado con DNI " + dni + "?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
    
        if (respuesta == JOptionPane.YES_OPTION) {
            boolean exito = gestionEmpleado.eliminar(dni);
        
           if (exito) {
                JOptionPane.showMessageDialog(this, "Empleado eliminado correctamente del sistema.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                listarEmpleados();
                btnLimpiarActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar. Verifique que el DNI ingresado exista.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

