    private void listarDestinos() {
        modelo.setRowCount(0); 
        for (int i = 0; i < gestionDestino.obtenerTotalDestinos(); i++) {
            DestinoTuristico dest = gestionDestino.obtenerDestino(i);
            
            Object[] fila = new Object[6];
            fila[0] = dest.getNombre();
            fila[1] = dest.getPais();
            fila[2] = dest.getCiudad();
            fila[3] = dest.getClima();
            fila[4] = dest.getIdiomaPrincipal();
            fila[5] = dest.getDescripcion();
            
            modelo.addRow(fila);
        }
    }

    
