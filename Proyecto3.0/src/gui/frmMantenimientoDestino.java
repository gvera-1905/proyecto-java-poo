    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText().trim();

        if (nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese o busque el nombre del destino a eliminar.");
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(this, "¿Está seguro de eliminar este destino?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            if (gestionDestino.eliminar(nombre)) {
                JOptionPane.showMessageDialog(this, "Destino eliminado con éxito.");
                listarDestinos();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "El destino no pudo ser eliminado.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

