    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText().trim();
        String pais = txtPais.getText().trim();
        String ciudad = txtCiudad.getText().trim();
        String clima = txtClima.getText().trim();
        String idioma = txtIdioma.getText().trim();
        String descripcion = txtDescripcion.getText().trim();

        if (nombre.isEmpty() || pais.isEmpty() || ciudad.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, llene los campos obligatorios (Nombre, País, Ciudad).");
            return;
        }

        DestinoTuristico nuevoDestino = new DestinoTuristico(nombre, pais, ciudad, descripcion, clima, idioma,"");

        if (gestionDestino.registrar(nuevoDestino)) {
            JOptionPane.showMessageDialog(this, "Destino registrado con éxito.");
            listarDestinos();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Error: El destino ya existe o no hay espacio.");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String nombreBuscar = txtNombre.getText().trim();

        if (nombreBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del destino a buscar.");
            return;
        }

        DestinoTuristico encontrado = gestionDestino.buscarPorNombre(nombreBuscar);

        if (encontrado != null) {
            txtPais.setText(encontrado.getPais());
            txtCiudad.setText(encontrado.getCiudad());
            txtClima.setText(encontrado.getClima());
            txtIdioma.setText(encontrado.getIdiomaPrincipal());
            txtDescripcion.setText(encontrado.getDescripcion());
        
            
            txtNombre.setEditable(false); 
        } else {
            JOptionPane.showMessageDialog(this, "Destino no encontrado.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

