    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        String dni = txtDni.getText().trim();
    String nombres = txtNombres.getText().trim();
    String apellidos = txtApellidos.getText().trim();
    String fechaNac = txtFechaNacimiento.getText().trim();
    String nacionalidad = txtNacionalidad.getText().trim();
    String telefono = txtTelefono.getText().trim();
    String correo = txtCorreo.getText().trim();

    Cliente clienteAct = new Cliente(dni, nombres, apellidos, fechaNac, nacionalidad, telefono, correo);

    if (gestionCliente.actualizar(clienteAct)) {
        JOptionPane.showMessageDialog(this, "Datos del cliente actualizados.");
        listarClientes();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "No se pudo actualizar. Verifique el DNI.");
    }
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String dni = txtDni.getText().trim();
    String nombres = txtNombres.getText().trim();
    String apellidos = txtApellidos.getText().trim();
    String fechaNac = txtFechaNacimiento.getText().trim();
    String nacionalidad = txtNacionalidad.getText().trim();
    String telefono = txtTelefono.getText().trim();
    String correo = txtCorreo.getText().trim();

    if (dni.isEmpty() || nombres.isEmpty() || apellidos.isEmpty()) {
        JOptionPane.showMessageDialog(this, "DNI, Nombres y Apellidos son campos obligatorios.");
        return;
    }

    Cliente nuevoCliente = new Cliente(dni, nombres, apellidos, fechaNac, nacionalidad, telefono, correo);

    if (gestionCliente.registrar(nuevoCliente)) {
        JOptionPane.showMessageDialog(this, "Cliente registrado con éxito.");
        listarClientes();
        limpiarCampos();
    } else {
        JOptionPane.showMessageDialog(this, "Error: El DNI ya existe o el arreglo está lleno.");
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String dniBuscar = txtDni.getText().trim();

    if (dniBuscar.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese un DNI para buscar.");
        return;
    }

    Cliente encontrado = gestionCliente.buscarPorDni(dniBuscar);

    if (encontrado != null) {
        txtNombres.setText(encontrado.getNombres());
        txtApellidos.setText(encontrado.getApellidos());
        txtFechaNacimiento.setText(encontrado.getFechaNacimiento());
        txtNacionalidad.setText(encontrado.getNacionalidad());
        txtTelefono.setText(encontrado.getTelefono());
        txtCorreo.setText(encontrado.getCorreo());
        txtDni.setEditable(false);
    } else {
        JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String dni = txtDni.getText().trim();

    if (dni.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese el DNI del cliente a eliminar.");
        return;
    }

    int resp = JOptionPane.showConfirmDialog(this, "¿Seguro de eliminar este cliente?", "Confirmar", JOptionPane.YES_NO_OPTION);
    if (resp == JOptionPane.YES_OPTION) {
        if (gestionCliente.eliminar(dni)) {
            JOptionPane.showMessageDialog(this, "Cliente eliminado.");
            listarClientes();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo eliminar.");
        }
    }
    }//GEN-LAST:event_btnEliminarActionPerformed

