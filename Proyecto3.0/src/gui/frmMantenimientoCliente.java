    void listarClientes() {
        modelo.setNumRows(0);
        for (int i = 0; i < gestionCliente.obtenerTotalClientes(); i++) {
            Cliente c = gestionCliente.obtenerCliente(i);
            
            Object[] fila = new Object[7];
            fila[0] = c.getDNI();
            fila[1] = c.getNombres();
            fila[2] = c.getApellidos();
            fila[3] = c.getFechaNacimiento();
            fila[4] = c.getNacionalidad();
            fila[5] = c.getTelefono();
            fila[6] = c.getCorreo();
            
            modelo.addRow(fila);
        }
    }

