    void listarPaquetes() {
        modelo.setNumRows(0);
        for (int i = 0; i < gestionPaquete.obtenerTotalPaquetes(); i++) {
            PaqueteTuristico paq = gestionPaquete.obtenerPaquete(i);
            
            Object[] fila = new Object[11];
            fila[0] = paq.getCodigo();
            fila[1] = paq.getNombre();
            fila[2] = paq.getDestino();
            fila[3] = paq.getDuracionDias();
            fila[4] = paq.getTipo();
            fila[5] = paq.getPrecioPorPersona();
            fila[6] = paq.getCupoMaximo();
            fila[7] = paq.getFechaSalida();
            fila[8] = paq.getFechaRetorno();
            fila[9] = paq.getEstado();
            
            modelo.addRow(fila);
        }
    }

