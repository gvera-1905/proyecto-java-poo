    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText().trim();
        String nombre = txtNombre.getText().trim();
        String destino = txtDestino.getText().trim();
        int duracion = Integer.parseInt(txtDuracion.getText().trim());
        String tipo = txtTipo.getText().trim();
        double precio = Double.parseDouble(txtPrecio.getText().trim());
        int cupoMaximo = Integer.parseInt(txtCupoMaximo.getText().trim());
        String fechaSalida = txtFechaSalida.getText().trim();
        String fechaRetorno = txtFechaRetorno.getText().trim();
        String estado = txtEstado.getText().trim();

        if (codigo.isEmpty() || nombre.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Código y Nombre son obligatorios.");
            return;
        }

       
        PaqueteTuristico nuevoPaquete = new PaqueteTuristico(codigo, nombre, destino, duracion, tipo, precio, cupoMaximo, fechaSalida, fechaRetorno, estado);

        if (gestionPaquete.registrar(nuevoPaquete)) {
            JOptionPane.showMessageDialog(this, "Paquete registrado con éxito.");
            listarPaquetes();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Error: El código ya existe o no hay espacio.");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigoBuscar = txtCodigo.getText().trim();

        if (codigoBuscar.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese un código para buscar.");
            return;
        }

        PaqueteTuristico encontrado = gestionPaquete.buscarPorCodigo(codigoBuscar);

        if (encontrado != null) {
            txtNombre.setText(encontrado.getNombre());
            txtDestino.setText(encontrado.getDestino());
            txtDuracion.setText(String.valueOf(encontrado.getDuracionDias()));
            txtTipo.setText(encontrado.getTipo());
            txtPrecio.setText(String.valueOf(encontrado.getPrecioPorPersona()));
            txtCupoMaximo.setText(String.valueOf(encontrado.getCupoMaximo()));
            txtFechaSalida.setText(encontrado.getFechaSalida());
            txtFechaRetorno.setText(encontrado.getFechaRetorno());
            txtEstado.setText(encontrado.getEstado());
            txtCodigo.setEditable(false);
        } else {
            JOptionPane.showMessageDialog(this, "Paquete no encontrado.");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

