    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String codigo = txtCodigo.getText().trim();

        if (codigo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el código del paquete a eliminar.");
            return;
        }

        int respuesta = JOptionPane.showConfirmDialog(this, "¿Seguro de eliminar este paquete?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (respuesta == JOptionPane.YES_OPTION) {
            if (gestionPaquete.eliminar(codigo)) {
                JOptionPane.showMessageDialog(this, "Paquete eliminado.");
                listarPaquetes();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el paquete.");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

